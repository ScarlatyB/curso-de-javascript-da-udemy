//Operador de incremento. O ++ pode ser tanto antes como dps da variável
let contador=1;
contador++;//2
contador++; //3
contador++; //4
contador++; //5
contador++; //6
console.log(contador);

//Operador de decremento. O -- pode ser tanto antes como dps da variável
let contador=1;
contador--;//2
contador--; //3
contador--; //4
contador--; //5
contador--; //6
console.log(contador);






